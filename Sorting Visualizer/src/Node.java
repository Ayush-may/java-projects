
public class Node {
	
	private int i,j;
	public int x,y;
	
	Node(int x,int y){
		this.i = i;		//
		this.j = j;		//
		this.x = x; 	//X-coordinate
		this.y = y;		//Y-coordinate
	}
	
	public int getI() {
		return i;
	}
	
	public int getJ() {
		return j;
	}
	
	public int getX() {
		return x;
	}
	
	public int getY() {
		return y;
	}
}
